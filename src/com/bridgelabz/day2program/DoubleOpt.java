package com.bridgelabz.day2program;

public class DoubleOpt {
	public static void main(String[] args) {
		double a = 5.3;
		double b = 3.4; 
		double c = 7.5;
		
		System.out.println("a + b * c = " + (a + b *c));
		System.out.println("a * b + c = " + (a * b + c));
		System.out.println("c + a / b = " + (c + a / b));
		System.out.println("a % b + c = " + (a % b + c));
	}
}
